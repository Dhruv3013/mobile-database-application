USE SECTION6;

--LOCATION_INFO TABLE CREATION
CREATE TABLE LOCATIONS_INFO(
    ADDRESS VARCHAR(50) PRIMARY KEY NOT NULL,       --DONE
    TELEPHONE VARCHAR(20) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL
);


--AMENITIES TABLE CREATION
CREATE TABLE AMENITIES(
    ADDRESS VARCHAR(50) PRIMARY KEY NOT NULL,
    AMENITY_NAME CHAR(50) NOT NULL,                 --DONE
    FOREIGN KEY (ADDRESS)
    REFERENCES LOCATIONS_INFO(ADDRESS)
);


--LOCATION TABLE CREATION
CREATE TABLE LOCATIONS(
    LOCATION_ID INTEGER PRIMARY KEY NOT NULL,
    NAME CHAR(50) NOT NULL,
    TIMES_VISITED INTEGER,
    ADDRESS VARCHAR(50) NOT NULL,       --DONE
    FOREIGN KEY (ADDRESS)
    REFERENCES LOCATIONS_INFO(ADDRESS)
);


--COACH TABLE CREATION
CREATE TABLE COACH (
    COACH_ID INTEGER PRIMARY KEY NOT NULL,
    COACH_NAME CHAR(50) NOT NULL,
    ADDRESS VARCHAR(50) NOT NULL,
    LEVEL INTEGER NOT NULL,
    RATING INTEGER NOT NULL,        --DONE
    BIODATA VARCHAR(500),
    FOREIGN KEY(ADDRESS)
    REFERENCES LOCATIONS_INFO(ADDRESS)
);


--COACH_CERTIFICATE TABLE CREATION
CREATE TABLE COACH_CERTIFICATE(
    CERTIFICATE_ID INTEGER PRIMARY KEY NOT NULL,        --DONE
    COACH_ID INTEGER NOT NULL,
    CERTIFICATES CHAR(50) NOT NULL,
    FOREIGN KEY (COACH_ID)
    REFERENCES COACH(COACH_ID)
);


--REFRENCES TABLE CREATION
CREATE TABLE REFRENCES(
    COACH_ID INTEGER PRIMARY KEY NOT NULL,
    CLIENT CHAR(50) NOT NULL,               --DONE
    CLIENT_EMAIL VARCHAR(50),
    PHONE_NO VARCHAR(20),
    FOREIGN KEY (COACH_ID)
    REFERENCES COACH(COACH_ID)
);


--REVIEWS TABLE CREATION
CREATE TABLE REVIEWS(
    COACH_ID INTEGER PRIMARY KEY NOT NULL,
    REVIEW_DATE DATE NOT NULL,
    COMMUNICATION INTEGER NOT NULL,
    ENTHUSIASM INTEGER NOT NULL,
    PUNCTUALITY INTEGER NOT NULL,
    DESCRIPTION VARCHAR(500),
    FOREIGN KEY(COACH_ID)
    REFERENCES COACH(COACH_ID)
);


--COACH_LOCATION TABLE CREATION
CREATE TABLE COACH_LOCATION(
    COACH_ID INTEGER NOT NULL,
    LOCATION_ID INTEGER NOT NULL,
    PRIMARY KEY(COACH_ID, LOCATION_ID),
    FOREIGN KEY(COACH_ID)
    REFERENCES COACH(COACH_ID),
    FOREIGN KEY (LOCATION_ID)
    REFERENCES LOCATIONS(LOCATION_ID)
);


--CLASSES TABLE CREATION
CREATE TABLE CLASSES(
    DAY CHAR(10) NOT NULL,
    TIME VARCHAR(10) NOT NULL,
    CLASS_NAME CHAR(20),
    PRICE INTEGER NOT NULL,
    ROOM VARCHAR(20) NOT NULL,
    GYMID INTEGER NOT NULL,
    PRIMARY KEY (DAY, TIME),
    FOREIGN KEY (GYMID)
    REFERENCES LOCATIONS(LOCATION_ID)
);



